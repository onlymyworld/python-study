# 切片
s=['Nice','To','Meet','You']
print('s[:3]',s[:3]);
print('s[2:3]',s[2:3]);
print('s[-2:]',s[-2:]);
print('s[-2:-1]',s[-2:-1]);
l=list(range(100));
print('l:',l);
print('l[1:10]',l[1:10]);
print('l[-10:]',l[-10:]);
print('l[-10:-5]',l[-10:-5]);
print('l[::5]',l[::5]);
print('l[:10:2]',l[:10:2]);
print('tuple',(1,2,3,4,5)[:3]);
string='String';
print('string[:2]',string[:2]);
print('string[-2:]',string[-2:]);
print('string[::2]',string[::2]);

